require 'json'

class CommentTypeDevSeeder
  COMMENT_TYPES = [
    {
      name: "Comment",
      description: "A general comment about the dance."
    }, {
      name: "Style Point",
      description: "Needs describing."
    }, {
      name: "Variation",
      description: "Needs describing."
    }
  ]

  def self.seed!
    COMMENT_TYPES.each do |ct_params|
      type = CommentType.find_or_initialize_by(name: ct_params[:name])

      type.update_attributes!(ct_params)
    end
  end
end
